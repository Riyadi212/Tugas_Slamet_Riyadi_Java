public class Variable {
    
    public static void main(String[] args) {
        
        //  VARIABLE
        //  * Variable adalah tempat untuk menyimpan data.
        //  * Java adalah bahasa static type, sehingga sebuah variable hanya bisa digunakan untuk menyimpan tipe data yang sama, 
        //    tidak bisa berubah-ubah tipe data seperti dibahasa pemograman PHP atau JavaScrip.
        //  * Untuk membuat variable di java kita bisa menggunakan nama tipe data lalu diikuti dengan nama variablenya
        //  * Nama variable tidak boleh mengandung whitespace(spasi,enter,tab) dan tidak boleh seluruhnya number

        String name;
        name = "Slamet Riyadi";
        System.out.println(name);

        int age = 29;
        System.out.println(age);

        String address = "Rawa Lele";
        System.out.println(address);

        name = "Salsa Putri Riyadi";
        System.out.println(name);














    }
}
