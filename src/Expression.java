
import java.util.Date;

public class Expression {
    
    public static void main(String[] args) {
        
        // Expression 
        // * Expression adalah konstruksi dari variable , operator dan pemanggilan methode yang mengevaluasi menhjadi sebuah single value 
        // * Expression adalah core component dari statement


        int value;
        value = 10;

        System.out.println(value = 100);

        
        // Statement
        // Statement bisa dibilang adalah kalimat lengkap dalam bahasa.
        // Sebuah statement berisikan execution komplit, biasanya diakhiri dengan titik koma.
        // Ada beberapa jenis statement:
        // * Assignment expression
        // * Penggunaan ++ dan --
        // * Method invocation
        // * Object creation expression


        // contoh kode Assigment statement
        double aValue = 8933.234;
        
        // Increment statement
        aValue++;

        // Methode invocation satatement
        System.out.println("Hello Word");

        // object creation statement
        Date date = new Date();

        







        
    











    }
}
